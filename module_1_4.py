# Как работает программа:
# Метод upper() преобразует строку в верхний регистр и выводит результат.

# Вывод строки в нижнем регистре
print("Нижний регистр:", my_string.lower())

# Как работает программа:
# Метод lower() преобразует строку в нижний регистр и выводит результат.

# Удаление всех пробелов из строки
